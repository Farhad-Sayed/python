# Keyword arguments are identified by the keyword put before them in the function call.
# Keyword arguments are optional parameters.
# For example, the print() function has the optional parameters 'end' and 'sep' to specify what should be printed
# at the end of its arguments and between its arguments (separating them) respectively.

print('Hello', end='')
print('World!')

print('cats', 'dogs', 'mice')
print('cats', 'dogs', 'mice', sep=',')